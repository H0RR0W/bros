export type AttackType = 'phishing' | 'social-engineering' | 'mitm' | 'password' | 'deepfake';
export type Difficulty = 'rookie' | 'agent' | 'expert';

export interface Choice {
  id: string;
  text: string;
  isCorrect: boolean;
  explanation: string;
  hpDelta: number; // negative = damage, positive = bonus
}

export interface ScenarioStep {
  id: string;
  type: 'email' | 'chat' | 'browser' | 'phone' | 'notification';
  title: string;
  description: string;
  content: EmailContent | ChatContent | BrowserContent | PhoneContent | NotifContent;
  choices: Choice[];
  attackType: AttackType;
  cweId: string;
  owaspCategory: string;
}

export interface EmailContent {
  kind: 'email';
  from: string;
  fromReal: string; // actual email shown on hover
  subject: string;
  body: string;
  hasAttachment?: boolean;
  attachmentName?: string;
  hasLink?: boolean;
  linkText?: string;
  linkUrl?: string; // fake suspicious URL
}

export interface ChatContent {
  kind: 'chat';
  platform: string;
  sender: string;
  avatar: string;
  messages: { sender: string; text: string; time: string }[];
}

export interface BrowserContent {
  kind: 'browser';
  url: string;
  faviconColor: string;
  title: string;
  formFields: string[];
  hasPadlock: boolean;
}

export interface PhoneContent {
  kind: 'phone';
  caller: string;
  callerNumber: string;
  transcript: string[];
}

export interface NotifContent {
  kind: 'notification';
  app: string;
  icon: string;
  message: string;
  action: string;
}

export interface Mission {
  id: string;
  slug: string;
  env: 'office' | 'home' | 'wifi';
  title: string;
  subtitle: string;
  description: string;
  difficulty: Difficulty;
  attackTypes: AttackType[];
  steps: ScenarioStep[];
  maxHp: number;
  unlocked: boolean;
  completedBy?: number; // % of players completed
}

